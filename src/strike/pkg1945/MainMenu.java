    public MainMenu() {
        initComponents();
        high = new ArrayList<>();
        p = new ArrayList<>();
        data = new ArrayList<>();
        
        File f = new File("Highscore.txt");
        if(!f.exists()) { 
            try{
                f.createNewFile();
                try{
                    FileOutputStream fo = new FileOutputStream("Highscore.txt");
                    ObjectOutputStream out = new ObjectOutputStream(fo);
                    out.writeObject(high);
                    out.close();
                    fo.close();
                }catch(IOException ex){
                    ex.printStackTrace();
                }      
            }catch(IOException ex){
                ex.printStackTrace();
            }
        }
        try{
            FileInputStream fin = new FileInputStream("Highscore.txt");
            ObjectInputStream in = new ObjectInputStream(fin);
            high = (ArrayList<HighScores>)in.readObject();
            in.close();
            fin.close();
        }catch(IOException ex){
            ex.printStackTrace();
        }catch(ClassNotFoundException ex2){
            ex2.printStackTrace();
        }
        
        File fi = new File("Player.txt");
        if(!fi.exists()) { 
            try{
                fi.createNewFile();
                try{
                    FileOutputStream fo = new FileOutputStream("Player.txt");
                    ObjectOutputStream out = new ObjectOutputStream(fo);
                    out.writeObject(p);
                    out.close();
                    fo.close();
                }catch(IOException ex){
                    ex.printStackTrace();
                }      
            }catch(IOException ex){
                ex.printStackTrace();
            }
        }
        try{
            FileInputStream fin = new FileInputStream("Player.txt");
            ObjectInputStream in = new ObjectInputStream(fin);
            p = (ArrayList<Player>)in.readObject();
            in.close();
            fin.close();
        }catch(IOException ex){
            ex.printStackTrace();
        }catch(ClassNotFoundException ex2){
            ex2.printStackTrace();
        }
        
        File fil = new File("Data.txt");
        if(!fil.exists()) { 
            try{
                fil.createNewFile();
                try{
                    FileOutputStream fo = new FileOutputStream("Data.txt");
                    ObjectOutputStream out = new ObjectOutputStream(fo);
                    out.writeObject(data);
                    out.close();
                    fo.close();
                }catch(IOException ex){
                    ex.printStackTrace();
                }      
            }catch(IOException ex){
                ex.printStackTrace();
            }
        }
        try{
            FileInputStream fin = new FileInputStream("Data.txt");
            ObjectInputStream in = new ObjectInputStream(fin);
            data = (ArrayList<inGameData>)in.readObject();
            in.close();
            fin.close();
        }catch(IOException ex){
            ex.printStackTrace();
        }catch(ClassNotFoundException ex2){
            ex2.printStackTrace();
        }
    }

